class EmployeeModel {
  final int empId;
  final String fullName;
  final String gender;
  final String department;
  final String username;
  final String password;

  EmployeeModel({
    required this.empId,
    required this.fullName,
    required this.gender,
    required this.department,
    required this.username,
    required this.password,
  });
}